def generate_hint(answer: str) -> str:
    if not answer:
        return ""
    first = answer[0]
    masked = ""
    for char in answer[1:]:
        if char.isalpha() or ('ã' <= char <= 'ã‚“') or ('ã‚¡' <= char <= 'ãƒ³') or ('ä¸€' <= char <= 'é¾¥'):
            masked += "ã€‡"
        elif char == " ":
            masked += "â–¡"
        else:
            masked += char
    return f"{first}{masked}ï¼ˆå…¨{len(answer)}æ–‡å­—ï¼‰"

